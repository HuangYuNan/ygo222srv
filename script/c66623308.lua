--娜娜的化妆师
require("/expansions/script/c37564765")
require("/expansions/script/c37564777")
function c66623308.initial_effect(c)
	senya.setreg(c,66623308,66623300)
	c:EnableReviveLimit()
	prim.nnr(c,c66623308.con,c66623308.op,aux.Stringid(66623308,0),CATEGORY_TODECK+CATEGORY_DRAW)
	local e4=Effect.CreateEffect(c)
	e4:SetType(EFFECT_TYPE_FIELD)
	e4:SetRange(LOCATION_MZONE)
	e4:SetTargetRange(0,LOCATION_MZONE)
	e4:SetCode(EFFECT_CANNOT_SELECT_BATTLE_TARGET)
	e4:SetValue(function(e,c)
		return c~=e:GetHandler() and c:IsHasEffect(66623300)
	end)
	c:RegisterEffect(e4)
	local e3=Effect.CreateEffect(c)
	e3:SetType(EFFECT_TYPE_FIELD)
	e3:SetCode(EFFECT_CANNOT_BE_EFFECT_TARGET)
	e3:SetRange(LOCATION_MZONE)
	e3:SetTargetRange(LOCATION_MZONE,0)
	e3:SetTarget(function(e,c)
		return c~=e:GetHandler() and c:IsHasEffect(66623300)
	end)
	e3:SetValue(aux.tgoval)
	c:RegisterEffect(e3)
end
function c66623308.con(e,tp,eg,ep,ev,re,r,rp)
	return Duel.IsExistingMatchingCard(c66623308.filter,tp,LOCATION_GRAVE,0,3,nil) and Duel.IsPlayerCanDraw(tp,1)
end
function c66623308.filter(c)
	return c:IsHasEffect(66623300) and c:IsAbleToDeck()
end
function c66623308.op(e,tp,eg,ep,ev,re,r,rp)
	Duel.Hint(HINT_SELECTMSG,tp,HINTMSG_SPSUMMON)
	local g=Duel.SelectMatchingCard(tp,c66623308.filter,tp,LOCATION_GRAVE,0,3,3,nil)
	Duel.SendtoDeck(g,nil,2,REASON_EFFECT)
	Duel.Draw(tp,1,REASON_EFFECT)
end